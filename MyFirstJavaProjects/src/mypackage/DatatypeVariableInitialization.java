package mypackage;

public class DatatypeVariableInitialization {

	public static void main(String[] args)
	{
		byte age=21;
		System.out.println(age);
		short no=3214;
		System.out.println(no);
		int bigger_no=76543;
		System.out.println(bigger_no);
		long elon_musk_salary=76543214;
		System.out.println(elon_musk_salary);
		
		
		float value1=4.65f;
		System.out.println(value1);
		double value2=3.3445435;
		System.out.println(value2);
		
		
		char a1='r';
		System.out.println(a1);
		
		boolean answer=true;
		System.out.println(answer);
		boolean answer1=false;
		System.out.println(answer1);
		
		String name="saroj kund";
		System.out.println(name);
		
		

	}

}
